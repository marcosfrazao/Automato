public class Transicoes {
    private ArrayList<String> origem;
    private String simbolo;
    private ArrayList<String> destino;

    public ArrayList<String> getOrigem() {
        return origem;
    }

    public void setOrigem(ArrayList<String> origem) {
        this.origem = origem;
    }

    public String getSimbolo() {
        return simbolo;
    }

    public void setSimbolo(String simbolo) {
        this.simbolo = simbolo;
    }

    public ArrayList<String> getDestino() {
        return destino;
    }

    public void setDestino(ArrayList<String> destino) {
        this.destino = destino;
    }
    
    
}
